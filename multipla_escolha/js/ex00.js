var dia

dia = parseInt(prompt("Digite o número do dia da semana correspondente:"))

switch(dia){
  case 1:{
    alert("Domingo")
    break
  }
   case 2:{
    alert("Segunda-feira")
    break
  }
    case 3:{
    alert("Terça-feira")
    break
  }
   case 4:{
    alert("Quarta-feira")
    break
  }
   case 5:{
    alert("quinta-feira")
    break
  }
   case 6:{
    alert("Sexta-feira")
    break
  }
   case 7:{
    alert("Sabado")
    break
  }
  default:{
    alert("opção inválida")
    break
  }
   
}